package org.wso2.carbon.cloud.billing.core.beans.usage;

/**
 * Bean that  represent the Account object
 */
public class Usage {

    private String accountId;
    private String uom;
    private int qty;
    private String subscriptionId;
    private String chargeId;
    private String description;
    private String startDate;
    private String endDate;
    private double unitPrice;
    private String tenantDomain;

    public String getAccountId() {
        return accountId;
    }

    public void setAccountId(String accountId) {
        this.accountId = accountId;
    }

    public String getUom() {
        return uom;
    }

    public void setUom(String uom) {
        this.uom = uom;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }

    public String getSubscriptionId() {
        return subscriptionId;
    }

    public void setSubscriptionId(String subscriptionId) {
        this.subscriptionId = subscriptionId;
    }

    public String getChargeId() {
        return chargeId;
    }

    public void setChargeId(String chargeId) {
        this.chargeId = chargeId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getEndDate() {
        return endDate;
    }

    public void setEndDate(String endDate) {
        this.endDate = endDate;
    }

    public double getUnitPrice() {
        return unitPrice;
    }

    public void setUnitPrice(double unitPrice) {
        this.unitPrice = unitPrice;
    }

    public String getTenantDomain() {
        return tenantDomain;
    }

    public void setTenantDomain(String tenantDomain) {
        this.tenantDomain = tenantDomain;
    }

    @Override
    public String toString() {
        return "accountId=" + accountId + ",uom=" + uom + ",qty=" + qty + ",subscriptionId=" + subscriptionId + "," +
               "chargeId=" +
               chargeId + ",description=" + description + ",startDate=" + startDate + ",endDate=" + endDate;
    }

}
